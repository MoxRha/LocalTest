Route::get('/', function () {
    return view('welcome');
});


Route::middleware(['auth', 'verified'])->group(function () {
    Route::get('/dashboard', [UserController::class, 'index'])->name('dashboard');
    Route::get('/dashboard/edituser/{id}', [UserController::class, 'edit'])->name('edit');
    Route::put('/dashboard/updateuser/{id}', [UserController::class, 'update'])->name('update');
    Route::get('/dashboard/deleteuser/{id}', [UserController::class, 'destroy'])->name('destroy');
});



Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

